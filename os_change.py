def future_risk_score(future_stress):
    """
    Measures how risky the future looks.
    """
    mean_future = np.mean(future_stress)
    max_future = np.max(future_stress)

    # Weighted risk
    risk = 0.6 * mean_future + 0.4 * max_future
    return round(risk, 3)

def agrios_step_with_forecast(
    static_stress,
    ml_stress,
    future_stress,
    os_state
):
    # Compute future risk
    risk = future_risk_score(future_stress)

    # Dynamic mode switching (OS policy)
    if risk > 0.65:
        os_state.mode = "STATIC"     # conservative
    elif risk < 0.35:
        os_state.mode = "ML"         # adaptive
    else:
        os_state.mode = "HYBRID"

    # Base decision
    if os_state.mode == "STATIC":
        chosen = static_stress
    elif os_state.mode == "ML":
        chosen = ml_stress
    else:
        chosen = 0.5 * static_stress + 0.5 * ml_stress

    # Smooth + guard
    smoothed = os_smooth(chosen, os_state.prev_stress)
    final = os_guard(smoothed)

    os_state.prev_stress = final

    return {
        "final_stress": round(final, 3),
        "mode": os_state.mode,
        "future_risk": risk
    }

