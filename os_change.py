class AgriOSState:
    def __init__(self, mode="HYBRID"):
        self.mode = mode          # STATIC | ML | HYBRID
        self.prev_stress = None   # memory

def os_smooth(current, previous, alpha=0.7):
    if previous is None:
        return current
    return alpha * current + (1 - alpha) * previous

"""Os protection"""

def os_guard(value, low=0.0, high=1.0):
    return max(low, min(high, value))

"""Agri os kernel step"""

def agrios_step(static_stress, ml_stress, os_state):
    # Mode selection
    if os_state.mode == "STATIC":
        chosen = static_stress
    elif os_state.mode == "ML":
        chosen = ml_stress
    else:  # HYBRID
        chosen = 0.5 * static_stress + 0.5 * ml_stress

    # Smooth with memory
    smoothed = os_smooth(chosen, os_state.prev_stress)

    # Apply safety bounds
    final = os_guard(smoothed)

    # Update OS memory
    os_state.prev_stress = final

    return round(final, 3)

