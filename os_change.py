def rf_uncertainty(model, X):
    """
    Returns mean prediction and standard deviation
    across trees.
    """
    tree_preds = np.array([
        tree.predict(X) for tree in model.estimators_
    ])
    mean = tree_preds.mean(axis=0)
    std = tree_preds.std(axis=0)
    return mean, std

