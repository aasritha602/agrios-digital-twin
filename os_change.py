def generate_actions(
    severity,
    sensors,
    future_risk
):
    actions = []

    # Moisture-based advice
    if sensors["soil_moisture"] < crop_row["min_moisture"]:
        actions.append("Increase irrigation volume")

    # Temperature stress
    if sensors["temperature"] > crop_row["max_temperature"]:
        actions.append("Provide shade or adjust planting time")

    # Nitrogen stress (soft)
    if sensors["nitrogen"] < nutrients_df["nitrogen"].mean():
        actions.append("Consider nitrogen supplementation")

    # Future-aware advice
    if future_risk > 0.6:
        actions.append("Act early due to rising future stress")

    if severity == "SAFE":
        actions.append("Maintain current practice")

    return actions

def priority_score(severity, future_risk):
    base = {
        "SAFE": 1,
        "WATCH": 2,
        "ALERT": 3,
        "CRITICAL": 4
    }[severity]

    return base + round(future_risk, 2)

