class QAgent:
    def __init__(self, state_size, action_size):
        self.q = {}
        self.lr = 0.1
        self.gamma = 0.95
        self.epsilon = 0.2
        self.action_size = action_size

    def get_key(self, state):
        return tuple(np.round(state, 2))

    def act(self, state):
        if np.random.rand() < self.epsilon:
            return np.random.choice(self.action_size)
        return np.argmax(self.q.get(self.get_key(state), np.zeros(self.action_size)))

    def learn(self, state, action, reward, next_state):
        key = self.get_key(state)
        next_key = self.get_key(next_state)

        self.q.setdefault(key, np.zeros(self.action_size))
        self.q.setdefault(next_key, np.zeros(self.action_size))

        self.q[key][action] += self.lr * (
            reward + self.gamma * np.max(self.q[next_key]) - self.q[key][action]
        )

